function fillYearsDropdown(years){
	//Filling the base layers dropdown
	var select = $("#dn_years");
	for(var i=0;i<years.length;i++){
		var option = new Option(years[i], years[i]);
		select.append(option);
	}
}

function fillProductsDropdown(products){
	//Filling the base layers dropdown
	var select = $("#dn_products");
	for(var i=0;i<products.length;i++){
		var option = new Option(products[i], products[i]);
		select.append(option);
	}
}

